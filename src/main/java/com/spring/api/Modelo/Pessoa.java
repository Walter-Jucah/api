package com.spring.api.Modelo;

import javax.persistence.Entity;

@Entity
public class Pessoa {

    private int codigo;
    private String nome;
    private int idade;

    public String getNome() {
        return nome;
    }

    public void setNome(String nome) {
        this.nome = nome;
    }

    public int getIdade() {
        return idade;
    }

    public void setIdade(int idade) {
        this.idade = idade;
    }

}
